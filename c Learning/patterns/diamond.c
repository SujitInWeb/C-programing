#include<stdio.h>
#include<math.h>
int main()
{
    int i,j,k,l,n,m;
//------------------------------1st part----------------------------------------------------------------------------
    for(i=6;i>=0;i--)
    {
        for(j=0;j<=i;j++)
        printf("*");
        for(k=6;k>=j;k--)
        printf(" ");
        for(l=6;l>=j;l--)
        printf(" ");
        for(m=0;m<=i;m++)
        printf("*");
    printf("\n");
    }
//-------------------------------2nd part----------------------------------------------------------------------------  
    for(i=0;i<=6;i++)
    {
        for(j=0;j<=i;j++)
        printf("*");
        for(k=6;k>=j;k--)
        printf(" ");
        for(l=6;l>=j;l--)
        printf(" ");
        for(n=0;n<=i;n++)
        printf("*");
    printf("\n");
    }
//----------------------------------------------------------------------------------------------------------------------------
return 0;
}
